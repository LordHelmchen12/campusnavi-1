package de.fhl.campusnavi;

public class Wc {
	private int id;
	private WcTyp typ;
	private POI poi;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public WcTyp getTyp() {
		return typ;
	}
	public void setTyp(WcTyp typ) {
		this.typ = typ;
	}
	public POI getPoi() {
		return poi;
	}
	public void setPoi(POI poi) {
		this.poi = poi;
	}

}
